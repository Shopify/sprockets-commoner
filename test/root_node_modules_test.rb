require 'test_helper'

class RootNodeModulesTest < MiniTest::Test
  def setup
    @env = Sprockets::Environment.new(File.join(__dir__, 'fixtures'))
    @env.append_path File.join(@env.root, 'root-node-modules', 'assets')
    @env.append_path File.join(@env.root, 'root-node-modules', 'node_modules')
  end

  def test_can_require_files_from_node_modules_in_root
    assert asset = @env['script.js']
    assert_equal <<-JS.chomp, asset.to_s.chomp
!function(global) {
var __commoner_initialize_module__ = function(f) {
  var module = {exports: {}};
  f.call(module.exports, module, module.exports);
  return module.exports;
};

var __commoner_module__root_node_modules$node_modules$empty_module$index_js = __commoner_initialize_module__(function (module, exports) {
  module.exports = null;
});
var __commoner_module__root_node_modules$assets$script_js = {};
}(typeof global != 'undefined' ? global : typeof window != 'undefined' ? window : this);
JS
  end
end
